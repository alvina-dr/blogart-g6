        <p>
            On voit l’émergence d’écoles supérieures privées moyennant un financement contre un diplôme promettant une carrière dans l’e-sport. Par exemple l’EGS (Education Gaming School), située à Bordeaux.
            <br>
            <br>
            Cette école propose une formation en 5 ans afin d’obtenir deux diplômes. Au bout de 3 ans on obtient un “bachelor” et à la fin de la 5ème année un “mastère”.<br>
            Le problème est que ces diplômes ne sont reconnus ni par l'État Français, ni par aucun autre pays.
           Ainsi, vos 5 ans dans cette école ne vaudront rien légalement.	Par ailleurs, l’EGS se vante d’avoir un parcours “original” avec leurs filières dans le digital, la communication et l’informatique.
        </p>
